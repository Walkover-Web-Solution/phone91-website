//for show hide div on hashchange
function showDiv(){
	//getting hash without hash
	var hash = window.location.hash.substring(1);
	// split by pipe
    hashSplit = hash.split('_');
	
	//dividing it into two part
	var url1 = hashSplit[0]; 
    var url2 = hashSplit[1];
	
	//firing function now
	if(url1 === 'mf'){
		//show hide div's
		$('.mfdnon').hide();
		$('#'+url2).show();
		
		//highlight the menus
		$('#mfnav li').removeClass('active');
		$('#mfnav li.'+url2).addClass('active');
	}
	else{
		//show hide div's
		$('.bfdnon').hide();
		$('#'+url2).show();
		
		//highlight the menus
		$('#bfnav li').removeClass('active');
		$('#bfnav li.'+url2).addClass('active');
	}
	//console.log(url1, url2);
}
